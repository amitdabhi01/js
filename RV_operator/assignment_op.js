// = assignment
// += add and assign 
// -= subtract and assign 
// *= multiply and assign 
// /= divide and assign 
// %= modulus and assign 
// **= exponent and assign  

let a = 10

console.log((a += 5)) // a = 10 + 5 = 15

console.log((a -= 5)) // a = 15 - 5 = 10

console.log((a *= 5)) // a = 10 * 5 = 50

console.log((a **= 5)) // a = 50 ** 5 = 312500000

console.log((a /= 5)) // a = 312500000 / 5 = 62500000

console.log((a %= 5)) // a = 62500000 % 5 = 0
