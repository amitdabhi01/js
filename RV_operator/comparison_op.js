// == equal (value only) 
// === strict equal (value + type)
// != not equal (value only)
// !== strict not equal (value + type)
// > greater than
// < less than
// >= greater than or equal
// <= less than or equal


let a = 5

let b = "5"

// check value  only ==
console.log(a == b)

// check value and datatypes ===
console.log(a === b)


let z = true 

// 0 = false
// 1 = true
console.log(z != 0)

// greater than 
let c = 10
console.log(a > c)

// less than
console.log(a < c)

// >=

console.log(a >= c)

// <= 
console.log(a <= c)